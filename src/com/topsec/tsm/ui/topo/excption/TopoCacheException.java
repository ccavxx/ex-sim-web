package com.topsec.tsm.ui.topo.excption;

public class TopoCacheException extends TopoException {
	private static final long serialVersionUID = 1L;

	public TopoCacheException() {
		super();
	}

	public TopoCacheException(String message) {
		super(message);
	}

	public TopoCacheException(Throwable cause) {
		super(cause);
	}

	public TopoCacheException(String message, Throwable cause) {
		super(message, cause);
	}  

}
