package com.topsec.tsm.sim.asset.exception;

public class LimitedNumException extends Exception {

	public LimitedNumException() {
		super();
	}


	public LimitedNumException(String message, Throwable cause) {
		super(message, cause);
	}

	public LimitedNumException(String message) {
		super(message);
	}

	public LimitedNumException(Throwable cause) {
		super(cause);
	}

}
